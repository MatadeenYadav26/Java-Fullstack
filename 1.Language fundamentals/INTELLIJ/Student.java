public class Student {
    String name;
    int age;
    int marks;


    Student(){
        //default
    }

    Student(String name,int age , int marks){
        this.name = name;
        this.age = age;
        this.marks = marks;
    }
    Student(String name,int age ){
        this.name = name;
        this.age = age;
        this.marks = marks;
    }
    Student(String name){
        this.name = name;
        this.age = age;
        this.marks = marks;
    }
}
